import jwt from "jsonwebtoken";

// =====================================================================================================================

// =========================================
// Decode JWT
// =========================================
export function decodeJwt<T extends object = Record<string, unknown>>(
  token: string
): T | null {
  try {
    const decoded = jwt.decode(token);
    return decoded ? (decoded as T) : null;
  } catch {
    return null;
  }
}

