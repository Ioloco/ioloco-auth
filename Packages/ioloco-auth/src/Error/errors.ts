export class AccessTokenExpiredError extends Error {
  constructor(message = 'Access token expsired, please log in again.') {
    super(message)
    this.name = 'AccessTokenExpiredError'
  }
}

// =====================================================================================================================
// =====================================================================================================================

export class RefreshTokenExpiredError extends Error {
  constructor(message = 'Refresh token expired, please log in again.') {
    super(message)
    this.name = 'RefreshTokenExpiredError'
  }
}

// =====================================================================================================================
// =====================================================================================================================

export class MissingCookieError extends Error {
  constructor(cookieName: string) {
    super(`Cookie '${cookieName}' is missing. Did you forget to set it in the response?`)
  }
}

