export async function refreshRoute() {
  const res = await fetch("/api/auth/refresh-cookie", {
    method: "POST",
    credentials: "include",
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({}),
  });

  if (!res.ok) {
    throw new Error("Session refresh failed");
  }
}

