import { z } from 'zod'

// =====================================================================================================================

// =========================================
// Access Token expired Error
// =========================================
export class AccessTokenExpiredError extends Error {
  constructor(message = 'Access token expsired, please log in again.') {
    super(message)
    this.name = 'AccessTokenExpiredError'
  }
}

// =====================================================================================================================
// =====================================================================================================================

// =========================================
// Refresh Token expired Error
// =========================================
export class RefreshTokenExpiredError extends Error {
  constructor(message = 'Refresh token expired, please log in again.') {
    super(message)
    this.name = 'RefreshTokenExpiredError'
  }
}

// =====================================================================================================================
// =====================================================================================================================

// =========================================
// Missing Cookie Error
// =========================================
export class MissingCookieError extends Error {
  constructor(cookieName: string) {
    super(`Cookie '${cookieName}' is missing. Did you forget to set it in the response?`)
  }
}

// =====================================================================================================================
// =====================================================================================================================

export class InvalidTokenError extends Error {
  constructor(zodError: z.ZodError) {
    super('Invalid Token')
    this.cause = zodError
  }
}

// =====================================================================================================================
// =====================================================================================================================

export class InvalidUserError extends Error {
  constructor(zodError: z.ZodError) {
    super('Invalid User')
    this.cause = zodError
  }
}

// =====================================================================================================================
// =====================================================================================================================

export class InvalidStateError extends Error {
  constructor() {
    super('Invalid State')
  }
}

// =====================================================================================================================
// =====================================================================================================================

export class InvalidCodeVerifierError extends Error {
  constructor() {
    super('Invalid Code Verifier')
  }
}

