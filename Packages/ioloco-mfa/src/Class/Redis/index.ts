import type { TempTokenStore } from '@Types/types'
import type IoRedis from 'ioredis'

// =====================================================================================================================

export class RedisTokenStore implements TempTokenStore {
  constructor(private readonly redis: IoRedis) {}

  // =====================================================================================================================
  // =====================================================================================================================

  async get(key: string): Promise<string | null> {
    return this.redis.get(key)
  }

  // =====================================================================================================================
  // =====================================================================================================================

  async set(key: string, value: string, expiresInSec = 300): Promise<void> {
    await this.redis.set(key, value, 'EX', expiresInSec)
  }

  // =====================================================================================================================
  // =====================================================================================================================

  async delete(key: string): Promise<void> {
    await this.redis.del(key)
  }

  // =====================================================================================================================
  // =====================================================================================================================
}
